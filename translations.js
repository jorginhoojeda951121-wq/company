const currentLang = document.getElementById("current-lang");
const menuToggle = document.getElementById("menuToggle");
const primaryNav = document.getElementById("primaryNav");
const navOverlay = document.getElementById("navOverlay");
const confirmBtn = document.getElementById("confirmBtn");
const cancelBtn = document.getElementById("cancelBtn");
const modal = document.getElementById("redirectModal");
let pendingUrl = null;

function setLanguage(lang) {
    const locale = translations[lang] || translations.en;

    document.documentElement.lang = lang;
    document.body.dir = locale.dir;

    document.querySelectorAll("[data-key]").forEach((el) => {
        const key = el.getAttribute("data-key");
        if (locale[key]) {
            el.innerHTML = locale[key];
        }
    });

    currentLang.textContent = `🌐 ${locale.lang_name}`;
    localStorage.setItem("preferredLang", lang);
}

function openModal(url) {
    if (!url || url === "#") {
        return;
    }
    pendingUrl = url;
    modal.classList.add("is-open");
    modal.setAttribute("aria-hidden", "false");
}

function closeModal() {
    pendingUrl = null;
    modal.classList.remove("is-open");
    modal.setAttribute("aria-hidden", "true");
}

document.querySelectorAll("[data-lang]").forEach((link) => {
    link.addEventListener("click", (event) => {
        event.preventDefault();
        setLanguage(link.dataset.lang);
    });
});

document.querySelectorAll("[data-url]").forEach((link) => {
    link.addEventListener("click", (event) => {
        event.preventDefault();
        openModal(link.dataset.url);
    });
});

function isMobileNav() {
    return window.matchMedia("(max-width: 1120px)").matches;
}

function closeMobileNav() {
    primaryNav.classList.remove("is-open");
    navOverlay.classList.remove("is-open");
    document.body.classList.remove("mobile-nav-open");
    menuToggle.setAttribute("aria-expanded", "false");
    menuToggle.textContent = "☰";
    document.querySelectorAll(".dropdown.is-open").forEach((item) => item.classList.remove("is-open"));
}

menuToggle.addEventListener("click", () => {
    const isOpen = primaryNav.classList.toggle("is-open");
    navOverlay.classList.toggle("is-open", isOpen);
    document.body.classList.toggle("mobile-nav-open", isOpen);
    menuToggle.setAttribute("aria-expanded", String(isOpen));
    menuToggle.textContent = isOpen ? "✕" : "☰";
    if (!isOpen) {
        document.querySelectorAll(".dropdown.is-open").forEach((item) => item.classList.remove("is-open"));
    }
});

navOverlay.addEventListener("click", closeMobileNav);

document.querySelectorAll(".dropdown > a").forEach((trigger) => {
    trigger.addEventListener("click", (event) => {
        if (!isMobileNav()) {
            return;
        }
        const parent = trigger.parentElement;
        const submenu = parent ? parent.querySelector(".dropdown-content") : null;
        if (!submenu) {
            closeMobileNav();
            return;
        }
        event.preventDefault();
        const willOpen = !parent.classList.contains("is-open");
        document.querySelectorAll(".dropdown.is-open").forEach((item) => item.classList.remove("is-open"));
        if (willOpen) {
            parent.classList.add("is-open");
        }
    });
});

window.addEventListener("resize", () => {
    if (!isMobileNav()) {
        closeMobileNav();
    }
});

confirmBtn.addEventListener("click", () => {
    if (pendingUrl) {
        window.open(pendingUrl, "_blank", "noopener,noreferrer");
    }
    closeModal();
});

cancelBtn.addEventListener("click", closeModal);

modal.addEventListener("click", (event) => {
    if (event.target === modal) {
        closeModal();
    }
});

document.addEventListener("keydown", (event) => {
    if (event.key === "Escape" && modal.classList.contains("is-open")) {
        closeModal();
    }
});

window.addEventListener("DOMContentLoaded", () => {
    const saved = localStorage.getItem("preferredLang") || "en";
    setLanguage(saved);
    closeMobileNav();
});